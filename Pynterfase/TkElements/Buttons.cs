using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace Pynterfase.TkElements
{
    public class Buttons
    {
        public int idButton { get; set; }
        public int x { get; set; }
        public int y { get; set; }
        public string Activebackground { get; set; }  // Establece el color de fondo cuando el widget está activo.
        public string Activeforeground { get; set; }  // Establece el color de primer plano cuando el widget está activo.
        public string Anchor { get; set; }  // Establece la posición de la cadena dentro del widget.
        public string Background { get; set; }  // Establece el color de fondo del widget.
        public string Bitmap { get; set; }  // Establece una imagen en blanco y negro como un mapa de bits para el widget.
        public string Borderwidth { get; set; }  // Establece el ancho del borde del widget.
        public string Compound { get; set; }  // Determina cómo se mostrará la imagen y el texto juntos en el widget.
        public string Cursor { get; set; }  // Establece el cursor del mouse cuando está sobre el widget.
        public string Default { get; set; }  // Establece el botón predeterminado para el widget.
        public string Disabledforeground { get; set; }  // Establece el color de primer plano cuando el widget está desactivado.
        public string Font { get; set; }  // Establece el tipo de letra para el widget.
        public string Foreground { get; set; }  // Establece el color de primer plano del widget.
        public string Height { get; set; }  // Establece la altura del widget.
        public string Highlightbackground { get; set; }  // Establece el color de fondo del widget cuando está resaltado.
        public string Highlightcolor { get; set; }  // Establece el color de resaltado del widget.
        public string Highlightthickness { get; set; }  // Establece el ancho del resaltado del widget.
        public string Image { get; set; }  // Establece una imagen para el widget.
        public string Justify { get; set; }  // Establece la justificación del texto dentro del widget.
        public string Padx { get; set; }  // Establece el relleno horizontal del widget.
        public string Pady { get; set; }  // Establece el relleno vertical del widget.
        public string Relief { get; set; }  // Establece el tipo de relieve para el borde del widget.
        public string State { get; set; }  // Establece el estado del widget (normal, desactivado, etc.).
        public string Takefocus { get; set; }  // Determina si el widget puede obtener el enfoque.
        public string Text { get; set; }  // Establece el texto del widget.
        public string Textvariable { get; set; }  // Establece una variable de cadena que se puede vincular al texto del widget.
        public string Underline { get; set; }  // Establece qué letra estará subrayada en el texto del widget.
        public string Width { get; set; }  // Establece el ancho del widget.
        public string Wraplength { get; set; }  // Establece la longitud de línea para el texto del widget antes de envolver.





    }
}